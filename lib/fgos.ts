function getGradeGroup(grade: number): GradeGroup {
  if (grade <= 6) return "5-6";
  if (grade <= 8) return "7-8";
  if (grade === 9) return "9";
  return "10-11";
}

export function getFgosContext(subject: string, grade: number): string {
  const subjectData = fgosData[subject];
  if (!subjectData) {
    return `Стандартные требования ФГОС для предмета ${subject}, ${grade} класс.`;
  }

  const group = getGradeGroup(grade);
  const data = subjectData[group];

  if (!data || data.competencies.length === 0) {
    return `Стандартные требования ФГОС для предмета ${subject}, ${grade} класс.`;
  }

  return `
Ключевые компетенции (${grade} класс):
${data.competencies.map((c, i) => `${i + 1}. ${c}`).join("\n")}

Рекомендуемая структура урока:
${data.lessonStructure.map((s, i) => `${i + 1}. ${s}`).join("\n")}

Критерии оценивания:
${data.assessmentCriteria.map((a, i) => `${i + 1}. ${a}`).join("\n")}
`.trim();
}
